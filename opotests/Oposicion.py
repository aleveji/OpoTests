class Oposicion:
    """
    Clase utilizada para representar una Oposicion

    ...

    Attributes
    ----------
    id : int
        identificador de la oposicion
    titulo : str
        titulo de la oposicion
    descripcion : str
        descripcion  de la oposicion
    """

    def __init__(self, id, titulo, descripcion):
        self.id = id
        self.titulo = titulo
        self.descripcion = descripcion
